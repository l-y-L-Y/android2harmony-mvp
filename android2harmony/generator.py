def _android_api_summary(usages: list[object]) -> dict[str, int]:
    summary: dict[str, int] = {}
    for usage in usages:
        category = str(getattr(usage, "category", "unknown"))
        summary[category] = summary.get(category, 0) + 1
    return dict(sorted(summary.items()))


def _quality_summary(project: AndroidProject, issues: list[MigrationIssue], retrofit_methods: list[dict[str, str]]) -> dict[str, object]:
    features = {feature for module in project.modules for feature in module.features}
    generated_points = 0
    reasons: list[str] = []
    if project.modules:
        generated_points += 15
        reasons.append("Harmony Stage 工程结构可生成。")
    if "android_api" in features:
        generated_points += 10
        reasons.append("Android API 使用点可扫描并生成兼容 facade。")
    if "network_api" in features and retrofit_methods:
        generated_points += 15
        reasons.append("Retrofit endpoint 可生成 HTTP client 与 MockServer。")
    if "room" in features:
        generated_points += 12
        reasons.append("Room Entity/DAO 可生成 schema 与 DAO adapter。")
    if "viewmodel" in features:
        generated_points += 10
        reasons.append("ViewModel/Flow 可生成 ArkTS Store 骨架。")
    if "hilt" in features:
        hilt_bindings = _discover_hilt_bindings(project)
        if hilt_bindings:
            generated_points += 8
            reasons.append("Hilt/Inject 可扫描并生成 ServiceRegistry 绑定清单。")
        else:
            reasons.append("Hilt 特征存在，但未识别到可生成的绑定点。")
    high_risks = len([issue for issue in issues if issue.severity == "high"])
    penalty = min(20, high_risks * 3)
    score = max(0, min(100, 35 + generated_points - penalty))
    if penalty:
        reasons.append(f"存在 {high_risks} 个高风险项，评分扣减 {penalty} 分。")
    reasons.append("评分只反映当前自动转译完成度，不等同于完整业务等价。")
    return {"score": score, "reasons": reasons}


