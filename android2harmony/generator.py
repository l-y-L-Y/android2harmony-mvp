def _discover_retrofit_methods(project: AndroidProject) -> list[dict[str, str]]:
    methods: list[dict[str, str]] = []
    seen_names: set[str] = set()
    for module in project.modules:
        for src in module.source_files:
            text = src.read_text(encoding="utf-8", errors="ignore")
            for match in re.finditer(r"@(GET|POST|PUT|DELETE|PATCH)\(\"([^\"]+)\"\)\s*suspend\s+fun\s+([A-Za-z0-9_]+)\s*\(", text, re.S):
                name = match.group(3)
                if name in seen_names:
                    continue
                seen_names.add(name)
                params = _extract_parenthesized(text, match.end() - 1)
                methods.append(
                    {
                        "verb": match.group(1),
                        "path": match.group(2),
                        "name": name,
                        "params": _retrofit_param_summary(params),
                    }
                )
    return methods


def _http_method_summary(project: AndroidProject) -> str:
    """Compact list of the real backend endpoints (from Retrofit) for the page prompt, so a
    screen that shows server-fetched data calls the live `MigratedHttpClient` method instead
    of seeding a mock array. e.g. `fetchPokemonList() -> GET pokemon (params: limit, offset)`."""
    parts: list[str] = []
    for m in _discover_retrofit_methods(project):
        desc = f"{m['name']}() -> {m['verb']} {m['path']}"
        if m.get("params"):
            desc += f" (params: {m['params']})"
        parts.append(desc)
    return "; ".join(parts)


def _extract_parenthesized(text: str, open_index: int) -> str:
    if open_index < 0 or open_index >= len(text) or text[open_index] != "(":
        return ""
    depth = 0
    for index in range(open_index, len(text)):
        char = text[index]
        if char == "(":
            depth += 1
        elif char == ")":
            depth -= 1
            if depth == 0:
                return text[open_index + 1 : index]
    return text[open_index + 1 :]


def _retrofit_param_summary(params: str) -> str:
    parts: list[str] = []
    for raw in _split_kotlin_params(params):
        item = " ".join(raw.strip().split())
        if not item:
            continue
        name_match = re.search(r"\b([A-Za-z0-9_]+)\s*:", item)
        param_name = name_match.group(1) if name_match else ""
        path_match = re.search(r"@Path\(\"([^\"]+)\"\)", item)
        query_match = re.search(r"@Query\(\"([^\"]+)\"\)", item)
        if path_match:
            parts.append(f"path:{path_match.group(1)}")
        elif query_match:
            parts.append(f"query:{query_match.group(1)}")
        elif "@Body" in item:
            parts.append(f"body:{param_name or 'body'}")
    return ",".join(parts)


def _harmony_request_method(verb: str) -> str:
    mapping = {
        "GET": "http.RequestMethod.GET",
        "POST": "http.RequestMethod.POST",
        "PUT": "http.RequestMethod.PUT",
        "DELETE": "http.RequestMethod.DELETE",
        "PATCH": "http.RequestMethod.PUT",
    }
    return mapping.get(verb.upper(), "http.RequestMethod.GET")


def _discover_hilt_bindings(project: AndroidProject) -> list[dict[str, str]]:
    bindings: list[dict[str, str]] = []
    seen: set[tuple[str, str, str]] = set()
    for module in project.modules:
        for src in module.source_files:
            text = src.read_text(encoding="utf-8", errors="ignore")
            rel = _project_relative(project, src)
            module_match = re.search(r"@Module\b[\s\S]{0,240}?\b(?:object|interface|class)\s+([A-Za-z0-9_]+)", text)
            module_name = module_match.group(1) if module_match else ""
            if module_name:
                _append_hilt_binding(bindings, seen, "module", module_name, module_name, rel)
            for match in re.finditer(r"@(Provides|Binds)\b[\s\S]{0,320}?\bfun\s+([A-Za-z0-9_]+)\s*\(([^)]*)\)\s*:\s*([A-Za-z0-9_<>.]+)", text):
                annotation = match.group(1)
                method = match.group(2)
                params = " ".join(match.group(3).split())
                target = _simple_type_name(match.group(4))
                name = f"{module_name}.{method}" if module_name else method
                _append_hilt_binding(bindings, seen, annotation.lower(), name, target, rel, params)
            for match in re.finditer(r"@Inject\s+constructor\s*\(([^)]*)\)", text):
                class_match = re.search(r"\bclass\s+([A-Za-z0-9_]+)\b", text[: match.start()][-800:])
                if class_match:
                    _append_hilt_binding(
                        bindings,
                        seen,
                        "inject-constructor",
                        class_match.group(1),
                        class_match.group(1),
                        rel,
                        " ".join(match.group(1).split()),
                    )
            for annotation, kind in [("@HiltViewModel", "hilt-viewmodel"), ("@AndroidEntryPoint", "android-entry-point")]:
                if annotation in text:
                    class_match = re.search(r"\bclass\s+([A-Za-z0-9_]+)\b", text)
                    if class_match:
                        _append_hilt_binding(bindings, seen, kind, class_match.group(1), class_match.group(1), rel)
    return bindings


def _append_hilt_binding(
    bindings: list[dict[str, str]],
    seen: set[tuple[str, str, str]],
    kind: str,
    name: str,
    target: str,
    source: str,
    params: str = "",
) -> None:
    key = (kind, name, target)
    if key in seen:
        return
    seen.add(key)
    bindings.append({"kind": kind, "name": name, "target": target, "source": source, "params": params})


def _simple_type_name(value: str) -> str:
    value = value.rsplit(".", 1)[-1]
    value = value.split("<", 1)[0]
    return _arkts_identifier(value)


