def _parse_kotlin_data_class(default_name: str, text: str) -> dict[str, object] | None:
    # Strip comments first: a // or /* */ comment between constructor params can contain a
    # comma that breaks param splitting and silently drops every field after it.
    text = re.sub(r"/\*.*?\*/", "", text, flags=re.S)
    text = re.sub(r"//[^\n]*", "", text)
    clean_text = re.sub(r"@\w+(?::\w+)?(?:\([^)]*\))?\s*", "", text)
    match = re.search(r"data\s+class\s+([A-Za-z0-9_]+)\s*\((.*?)\)\s*(?:[:{]|$)", clean_text, re.S)
    if not match:
        return None
    name = _arkts_identifier(match.group(1) or default_name)
    body = match.group(2)
    fields: list[tuple[str, str, str]] = []
    for raw in _split_kotlin_params(body):
        item = raw.strip().rstrip(",")
        item = re.sub(r"^(val|var)\s+", "", item)
        item = item.split("=", 1)[0].strip()
        field = re.match(r"([A-Za-z0-9_]+)\s*:\s*([^,]+)", item)
        if not field:
            continue
        field_name = field.group(1)
        kotlin_type = field.group(2).strip()
        optional = "?" if "?" in kotlin_type else ""
        fields.append((field_name, optional, _kotlin_type_to_arkts(kotlin_type)))
    if not fields:
        return None
    return {"name": name, "fields": fields}


def _split_kotlin_params(value: str) -> list[str]:
    parts: list[str] = []
    current: list[str] = []
    depth = 0
    for char in value:
        if char in "(<[":
            depth += 1
        elif char in ")>]" and depth > 0:
            depth -= 1
        if char == "," and depth == 0:
            parts.append("".join(current))
            current = []
        else:
            current.append(char)
    if current:
        parts.append("".join(current))
    return parts


def _kotlin_type_to_arkts(value: str) -> str:
    value = value.replace("?", "").strip()
    if value in {"String", "Char"}:
        return "string"
    if value in {"Int", "Long", "Float", "Double", "Short"}:
        return "number"
    if value == "Boolean":
        return "boolean"
    list_match = re.match(r"(List|MutableList)<(.+)>", value)
    if list_match:
        return f"{_kotlin_type_to_arkts(list_match.group(2))}[]"
    return "Object"


def _arkts_type_to_sql(value: str) -> str:
    if value == "number":
        return "INTEGER"
    if value == "boolean":
        return "INTEGER"
    if value.endswith("[]") or value == "Object":
        return "TEXT"
    return "TEXT"


def _field_is_primary_key(text: str, field_name: str) -> bool:
    return bool(re.search(r"@PrimaryKey[^\n]*(?:val|var)\s+" + re.escape(field_name) + r"\b", text))


def _discover_retrofit_base_url(project: AndroidProject) -> str | None:
    for module in project.modules:
        for src in module.source_files:
            text = src.read_text(encoding="utf-8", errors="ignore")
            match = re.search(r"\.baseUrl\(\s*\"([^\"]+)\"", text)
