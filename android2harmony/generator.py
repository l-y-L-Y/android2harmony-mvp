def _copy_resources(module: AndroidModule, output_dir: Path) -> list[Path]:
    copied: list[Path] = []
    media_root = output_dir / "entry" / "src" / "main" / "resources" / "base" / "media"
    raw_root = output_dir / "android_original" / module.name.replace(":", "_") / "res"
    for src in module.resource_files:
        if src.suffix.lower() in {".png", ".jpg", ".jpeg", ".webp", ".svg", ".ttf", ".otf"}:
            dest = media_root / _sanitize_media_filename(src.name)
            # HarmonyOS keys media by name without extension, so 'x.png' and 'x.webp'
            # collide. Keep the first; skip any later file with the same stem.
            if media_root.exists() and any(media_root.glob(dest.stem + ".*")):
                continue
        else:
            base_res = module.path / "src" / "main" / "res"
            if base_res.exists() and src.is_relative_to(base_res):
                rel = src.relative_to(base_res)
            else:
                rel = src.relative_to(module.path / "res")
            dest = raw_root / rel
        dest.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(src, dest)
        copied.append(dest)
    return copied


def _sanitize_media_filename(name: str) -> str:
    path = Path(name)
    stem = path.stem.replace(".9", "_9")
    stem = re.sub(r"[^A-Za-z0-9_]", "_", stem)
    stem = re.sub(r"_+", "_", stem).strip("_") or "media"
    return f"{stem}{path.suffix.lower()}"


def wire_sibling_imports(pages_dir: Path) -> int:
    """Auto-add `import { X } from './X'` when a page renders a sibling page component
    `X()` (an embedded fragment) but forgot the import. Deterministic and precise:
    only fires for names that are actual generated page files."""
    if not pages_dir.exists():
        return 0
    names = {p.stem for p in pages_dir.glob("*.ets")}
    wired = 0
    for p in pages_dir.glob("*.ets"):
        text = p.read_text(encoding="utf-8", errors="ignore")
        used = set(re.findall(r"\b([A-Z][A-Za-z0-9_]*)\(\)", text))
        needed = sorted(n for n in used if n in names and n != p.stem and f"from './{n}'" not in text)
        if needed:
            header = "".join(f"import {{ {n} }} from './{n}';\n" for n in needed)
            p.write_text(header + text, encoding="utf-8")
            wired += 1
    return wired


def _match_paren(s: str, open_idx: int) -> int:
    """Index of the ')' matching the '(' at open_idx, or -1 (string-literal aware enough
    for generated ArkTS: skips parens inside '...' and \"...\")."""
    depth = 0
    quote = ""
    i = open_idx
    while i < len(s):
        c = s[i]
        if quote:
            if c == "\\":
                i += 2
                continue
            if c == quote:
                quote = ""
        elif c in "'\"":
            quote = c
        elif c == "(":
            depth += 1
        elif c == ")":
            depth -= 1
            if depth == 0:
                return i
        i += 1
    return -1


_ROUTER_CALL_RE = re.compile(r"router\.(?:pushUrl|replaceUrl)\s*\(")


def _repair_navigation_targets(pages_dir: Path) -> int:
    """Stop HarmonyOS 'page does not exist' (router 100002) crashes from navigation the
    model invented. Two deterministic cases:
    - A capability ADAPTER used as if it were a route (e.g. a Share button rendered as
      `router.pushUrl({url:'pages/ShareCompat', params:{text: expr}})` - the model copied
      the page-navigation shape for a share action). Rewrite to the real adapter call
      `ShareCompat.shareText(getContext(this) as common.UIAbilityContext, expr)`.
    - Any other navigation whose target page was never generated: neutralize to a no-op so
      the tap does nothing instead of routing to a missing page and crashing."""
    if not pages_dir.exists():
        return 0
    valid = {f"pages/{p.stem}" for p in pages_dir.glob("*.ets")}
    fixed = 0
    for p in pages_dir.glob("*.ets"):
        text = p.read_text(encoding="utf-8", errors="ignore")
        orig = text
        share_needed = False
        out: list[str] = []
        i = 0
        while True:
            m = _ROUTER_CALL_RE.search(text, i)
            if not m:
                out.append(text[i:])
                break
            paren_close = _match_paren(text, m.end() - 1)
            if paren_close < 0:
                out.append(text[i:])
                break
            call = text[m.start():paren_close + 1]
            urlm = re.search(r"""url:\s*['"](pages/[A-Za-z0-9_]+)['"]""", call)
            out.append(text[i:m.start()])
            if urlm and urlm.group(1) not in valid:
                target = urlm.group(1)
                short = target.split("/", 1)[1]
                if short == "ShareCompat":
                    tm = re.search(r"text:\s*([^,}\n]+)", call) or re.search(r"title:\s*([^,}\n]+)", call)
                    expr = tm.group(1).strip() if tm else "''"
                    out.append(f"ShareCompat.shareText(getContext(this) as common.UIAbilityContext, {expr})")
                    share_needed = True
                else:
                    out.append(f"(() => {{ /* removed nav to missing page {target} */ }})()")
                fixed += 1
            else:
                out.append(call)
            i = paren_close + 1
        text = "".join(out)
        if share_needed:
            if "from '../platform/ShareCompat'" not in text:
                text = "import { ShareCompat } from '../platform/ShareCompat';\n" + text
            if "from '@kit.AbilityKit'" not in text:
                text = "import { common } from '@kit.AbilityKit';\n" + text
        if text != orig:
            p.write_text(text, encoding="utf-8")
    return fixed


def _match_brace(s: str, open_idx: int) -> int:
    """Index of the '}' matching the '{' at open_idx, or -1 (string-literal aware)."""
    depth = 0
    quote = ""
    i = open_idx
    while i < len(s):
        c = s[i]
        if quote:
            if c == "\\":
                i += 2
                continue
            if c == quote:
                quote = ""
        elif c in "'\"`":
            quote = c
        elif c == "{":
            depth += 1
        elif c == "}":
            depth -= 1
            if depth == 0:
                return i
        i += 1
    return -1


def _inline_sibling_stub_structs(pages_dir: Path) -> int:
    """A host page that embeds a fragment (`FragmentMain()`) sometimes also defines a LOCAL
    `struct FragmentMain` stub (e.g. one that renders only 'Main Content') instead of
    importing the real sibling page - the local definition shadows the import, so the host
    renders an empty placeholder instead of the actual screen. Excise any local `struct X`
    whose name matches a real sibling page and that is embedded as `X()`, and import the
    real one instead."""
    if not pages_dir.exists():
        return 0
    names = {p.stem for p in pages_dir.glob("*.ets")}
    fixed = 0
    for p in pages_dir.glob("*.ets"):
        text = p.read_text(encoding="utf-8", errors="ignore")
        self_stem = p.stem
        to_import: set[str] = set()
        for sib in names:
            if sib == self_stem:
                continue
            # embedded as a component AND declared locally -> a shadowing stub
            if not re.search(rf"\b{re.escape(sib)}\s*\(\s*\)", text):
                continue
            m = re.search(rf"\bstruct\s+{re.escape(sib)}\b", text)
            if not m:
                continue
            brace = text.find("{", m.start())
            if brace < 0:
                continue
            close = _match_brace(text, brace)
            if close < 0:
                continue
            start = text.rfind("\n", 0, m.start()) + 1
            while True:  # swallow leading decorator lines (@Component / @Entry / export)
                prev = text.rfind("\n", 0, start - 1) + 1
                if prev < start and text[prev:start - 1].lstrip().startswith("@"):
                    start = prev
                else:
                    break
            end = close + 1
            while end < len(text) and text[end] in "\r\n":
                end += 1
            text = text[:start] + text[end:]
            to_import.add(sib)
        if to_import:
            header = "".join(
                f"import {{ {n} }} from './{n}';\n" for n in sorted(to_import)
                if f"from './{n}'" not in text
            )
            p.write_text(header + text, encoding="utf-8")
            fixed += 1
    return fixed


def _available_media_names(project: AndroidProject) -> set[str]:
    """Media resource names (lowercased stems) that will exist in the generated project,
    so the LLM page agent only references images that actually resolve at build time."""
    names = {"foreground", "background", "starticon", "layered_image"}
    for module in project.modules:
        for src in module.resource_files:
            if src.suffix.lower() in {".png", ".jpg", ".jpeg", ".webp", ".svg"}:
                stem = Path(_sanitize_media_filename(src.name)).stem
                names.add(stem.lower())
    return names


def _expand_layout_sources(layout_file: Path, max_depth: int = 2) -> str:
    """Inline the layouts a screen references (`<include>` toolbars, RecyclerView
    `tools:listitem` item templates) so the model can render list cards and headers
    faithfully instead of guessing what each row looks like."""
    seen: set[str] = set()
    sections: list[str] = []

    def visit(path: Path, depth: int) -> None:
        if depth > max_depth or path.stem in seen or not path.exists():
            return
        seen.add(path.stem)
        text = path.read_text(encoding="utf-8", errors="ignore")
        label = path.name if not sections else f"referenced layout: {path.name}"
        sections.append(f"<!-- {label} -->\n{text}")
        for ref in re.findall(r"@layout/([A-Za-z0-9_]+)", text):
            if ref not in seen:
                visit(path.parent / f"{ref}.xml", depth + 1)

    visit(layout_file, 0)
    return "\n\n".join(sections)


def _layout_string_hints(layout_xml: str, strings: dict[str, str], limit: int = 60) -> str:
    """Resolve the @string/* references used in one layout into name->value lines,
    so the model reproduces the exact original UI text (incl. Chinese) instead of guessing."""
    lines: list[str] = []
    seen: set[str] = set()
    for key in re.findall(r"@string/([A-Za-z0-9_]+)", layout_xml):
        if key in seen:
            continue
        seen.add(key)
        value = strings.get(key)
        if value:
            lines.append(f"{key} = {value}")
        if len(lines) >= limit:
            break
    return "\n".join(lines)


