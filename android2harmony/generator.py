def _dao_param_entity_name(params: str) -> str | None:
    match = re.search(r":\s*(?:List<)?([A-Za-z0-9_]+)", params)
    if not match:
        return None
    return match.group(1)


def _dao_query_param_names(params: str) -> list[str]:
    names: list[str] = []
    for raw in _split_kotlin_params(params):
        item = raw.strip()
        match = re.search(r"(?:^|[\s)])([A-Za-z_][A-Za-z0-9_]*)\s*:", item)
        if match:
            names.append(_arkts_identifier(match.group(1)))
    return names


def _sql_table(sql: str) -> str | None:
    match = re.search(r"\bFROM\s+([A-Za-z0-9_]+)", sql, re.I)
    if match:
        return match.group(1)
    match = re.search(r"\bINTO\s+([A-Za-z0-9_]+)", sql, re.I)
    if match:
        return match.group(1)
    return None


def _entity_table_name(text: str, fallback: str) -> str:
    """Room's real SQLite table name from @Entity(tableName="..."); else the class name.

    _parse_kotlin_data_class strips annotations, so the table name must be read from the
    raw source. Callers only invoke this for files that contain @Entity.
    """
    match = re.search(r'tableName\s*=\s*"([^"]+)"', text)
    return match.group(1) if match else fallback


