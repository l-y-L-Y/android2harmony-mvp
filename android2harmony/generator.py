def _first_store_result_field(fields: list[dict[str, str]]) -> dict[str, str] | None:
    for field in fields:
        if field["type"].endswith("[]"):
            return field
    for field in fields:
        if field["type"].startswith("Object"):
            return field
    return None


def _parse_viewmodel_store(default_name: str, text: str) -> dict[str, object] | None:
    match = re.search(r"class\s+([A-Za-z0-9_]*ViewModel)\b", text)
    if not match:
        return None
    name = match.group(1) if match else default_name
    fields: list[dict[str, str]] = []
    seen: set[str] = set()

    for match in re.finditer(r"var\s+([A-Za-z0-9_]+)\s*:\s*([^=\n]+?)\s+by\s+bindingProperty\(([^)]*)\)", text):
        field = _state_field(match.group(1), match.group(2), match.group(3))
        if field["name"] not in seen:
            fields.append(field)
            seen.add(field["name"])

    for match in re.finditer(r"val\s+([A-Za-z0-9_]+)\s*:\s*([^=\n]+?)\s+by\s+[A-Za-z0-9_]+\.asBindingProperty\([^,]+,\s*([^)]+)\)", text):
        field = _state_field(match.group(1), match.group(2), match.group(3))
        if field["name"] not in seen:
            fields.append(field)
            seen.add(field["name"])

    for match in re.finditer(r"private\s+val\s+([A-Za-z0-9_]+)\s*:\s*MutableStateFlow<(.+?)>\s*=\s*MutableStateFlow\(([^)]*)\)", text):
        field = _state_field(match.group(1), match.group(2), match.group(3))
        if field["name"] not in seen:
            fields.append(field)
            seen.add(field["name"])

    actions = [
        _arkts_identifier(match.group(1))
        for match in re.finditer(r"\bfun\s+([A-Za-z0-9_]+)\s*\(", text)
        if match.group(1) not in {"provideFactory", "create"}
    ]
    actions = list(dict.fromkeys(actions))
    if not fields and not actions:
        return None
    return {"name": name, "fields": fields, "actions": actions}


def _state_field(name: str, kotlin_type: str, initial: str) -> dict[str, str]:
    nullable = "?" in kotlin_type
    arkts_type = _kotlin_type_to_arkts(kotlin_type.strip())
    if arkts_type == "Object" and "List<" in kotlin_type:
        arkts_type = "Object[]"
    value = _state_initial_value(initial.strip(), arkts_type)
    if value == "undefined" and not arkts_type.endswith("[]") and "undefined" not in arkts_type:
        arkts_type = f"{arkts_type} | undefined"
    elif nullable and not arkts_type.endswith("[]") and "undefined" not in arkts_type:
        arkts_type = f"{arkts_type} | undefined"
    return {"name": _arkts_identifier(name), "type": arkts_type, "initial": value}


def _state_initial_value(value: str, arkts_type: str) -> str:
    if value in {"false", "true"}:
        return value
    if value in {"null", "undefined"}:
        return "undefined"
    if value.startswith("emptyList"):
        return "[]"
    if re.match(r"^-?\d+(\.\d+)?$", value):
        return value
    if arkts_type == "string":
        cleaned = value.strip("\"'")
        return f"'{_escape(cleaned)}'" if cleaned else "''"
    if arkts_type.endswith("[]"):
        return "[]"
    if "?" in value:
        return "undefined"
    return "undefined" if arkts_type == "Object" else value


def _discover_permission_mappings(project: AndroidProject) -> list[dict[str, str]]:
    mappings: list[dict[str, str]] = []
    seen: set[str] = set()
    for module in project.modules:
        if not module.manifest or not module.manifest.exists():
            continue
        text = module.manifest.read_text(encoding="utf-8", errors="ignore")
        for match in re.finditer(r"<uses-permission[^>]+android:name=[\"']([^\"']+)[\"']", text):
            android_name = match.group(1)
            if android_name in seen:
                continue
            seen.add(android_name)
            harmony_name, status = _android_permission_to_harmony(android_name)
            mappings.append({"androidName": android_name, "harmonyName": harmony_name, "status": status})
    if not mappings and any("network_api" in module.features for module in project.modules):
        mappings.append({"androidName": "android.permission.INTERNET", "harmonyName": "ohos.permission.INTERNET", "status": "generated"})
    return mappings


def _android_permission_to_harmony(name: str) -> tuple[str, str]:
    table = {
        "android.permission.INTERNET": "ohos.permission.INTERNET",
        "android.permission.ACCESS_NETWORK_STATE": "ohos.permission.GET_NETWORK_INFO",
        "android.permission.CAMERA": "ohos.permission.CAMERA",
        "android.permission.RECORD_AUDIO": "ohos.permission.MICROPHONE",
        "android.permission.READ_MEDIA_IMAGES": "ohos.permission.READ_IMAGEVIDEO",
        "android.permission.READ_MEDIA_VIDEO": "ohos.permission.READ_IMAGEVIDEO",
        "android.permission.READ_MEDIA_AUDIO": "ohos.permission.READ_AUDIO",
        "android.permission.ACCESS_FINE_LOCATION": "ohos.permission.LOCATION",
        "android.permission.ACCESS_COARSE_LOCATION": "ohos.permission.APPROXIMATELY_LOCATION",
        "android.permission.POST_NOTIFICATIONS": "ohos.permission.NOTIFICATION_CONTROLLER",
    }
    if name in table:
        return table[name], "generated"
    if name.startswith("ohos.permission."):
        return name, "generated"
    return name.replace("android.permission.", "ohos.permission."), "adapter-required"


def _discover_resource_mappings(project: AndroidProject) -> list[dict[str, str]]:
    mappings: list[dict[str, str]] = []
    seen: set[str] = set()
    for module in project.modules:
        for src in module.resource_files:
            try:
                base_res = module.path / "src" / "main" / "res"
                if base_res.exists() and src.is_relative_to(base_res):
                    rel = src.relative_to(base_res)
                else:
                    rel = src.relative_to(module.path / "res")
            except ValueError:
                rel = src.name
            folder = rel.parts[0] if isinstance(rel, Path) and rel.parts else src.parent.name
            kind = folder.split("-", 1)[0]
            name = src.stem
            if kind == "values":
                mappings.extend(_value_resource_mappings(src, seen))
                continue
            android_name = f"{kind}.{name}"
            if android_name in seen:
                continue
            seen.add(android_name)
            harmony_kind = "media" if kind in {"drawable", "mipmap", "raw"} else kind
            mappings.append(
                {
                    "androidName": android_name,
                    "harmonyName": f"{harmony_kind}.{name}",
                    "kind": harmony_kind,
                    "source": _project_relative(project, src),
                }
            )
            if len(mappings) >= 500:
                return mappings
    return mappings


def _value_resource_mappings(src: Path, seen: set[str]) -> list[dict[str, str]]:
    mappings: list[dict[str, str]] = []
    try:
        root = ET.parse(src).getroot()
    except ET.ParseError:
        return mappings
    for item in root:
        name = item.attrib.get("name")
        if not name:
            continue
        kind = item.tag
        if kind == "item":
            kind = item.attrib.get("type", "element")
        android_name = f"{kind}.{name}"
        if android_name in seen:
            continue
        seen.add(android_name)
        harmony_kind = "element" if kind in {"string", "color", "dimen", "bool", "integer"} else kind
        mappings.append({"androidName": android_name, "harmonyName": f"{harmony_kind}.{name}", "kind": harmony_kind, "source": src.as_posix()})
    return mappings


